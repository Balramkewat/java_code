package google.com;

public class a_to_z {
    public static void main(String[] args) {
        //char x= 'A';
        for (char i='a';i<='z';i++)
        {
            System.out.format("%2c",i);

        }
        System.out.println();

        for (char j='A';j<='Z';j++)
        {
            System.out.format("%2c",j);

        }
        System.out.println();

    }
}
